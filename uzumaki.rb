#     6, 7,   18, 19, 20, 21,    38, 39, 40, 41, 42, 43
# 4. 下には一周戻った数字があるゾーン
#     8, 9,   22, 23, 24, 25,    44, 45, 46, 47, 48, 49
#     1  2     7   8   9  10     21  22  23  24  25  26
#     3 -> 7      5 -> 15            7 -> 23
#              22 - 9 + 3 - 1        44 - 25 + 5 - 1
#
