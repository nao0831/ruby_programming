#   2->5 2->7
#   3->3 3->5
#   4->1 4->3
#   5->3 5->1
#   6->5
#   7->7
def below_number(t, n, m,i)
  o=i>n/2?2*i-n :n+2-2*i
  t+(99.step(-1,-2).map{|s|
      u=s**2
      next(u+s<t&&t<u+s+s+3) ?4*o-1:
        u<t&&t<=u+s ?1:
        u-s-s+2<t&&t<u-s+2 ?-1:
        s<1?5-4*o :0
    }-[0])[0]
end

n=gets.to_i
m=n**2
r=[(m-n+1..m)]
n.times{|i|
  i!=0&&r<<r[i-1].map{|t|below_number t,n,m,i}
  puts r[i].map{|w|"%#{m.to_s.size}d"%w}*" "
}

